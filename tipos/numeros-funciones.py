print("math.floor(4.8):", math.floor(4.8))  # Output: 4
print("math.exp(1):", math.exp(1))  # Output: 2.718281828459045
print("math.log(10, 10):", math.log(10, 10))  # Output: 1.0
print("math.log10(100):", math.log10(100))  # Output: 2.0
print("math.sin(math.pi / 2):", math.sin(math.pi / 2))  # Output: 1.0
print("math.cos(math.pi):", math.cos(math.pi))  # Output: -1.0
print("math.tan(math.pi / 4):", math.tan(math.pi / 4))  # Output: 1.0
print("math.degrees(math.pi):", math.degrees(math.pi))  # Output: 180.0
print("math.radians(180):", math.radians(180))  # Output: 3.141592653589793
